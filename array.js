var age = [19, 30, 25, 35];
console.log(age);

// push 
age.push(28);
console.log(age);

// pop 
age.pop();
console.log(age);

// shift (remove the first value)
age.shift();
console.log(age);

// unshift (add a first value)
age.unshift(28);
console.log(age);

// map 
age.map((age) => console.log(age));